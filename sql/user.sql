create table hr_system.tb_users(
	id integer auto_increment primary key,
	user_name varchar(256),
	first_name varchar(256),
	last_name varchar(256),
	email varchar(256),
	password varchar(256),
	number varchar(10),
	age varchar(256),
	gender varchar(256),
	city varchar(256),
	category varchar(256)
);
